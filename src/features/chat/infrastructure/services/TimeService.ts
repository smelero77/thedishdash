import { TimeProvider } from '@/features/chat/domain/ports';

export class TimeService implements TimeProvider {
  getCurrentTime(): Date {
    return new Date();
  }

  getCurrentTimeOfDay(): 'morning' | 'afternoon' | 'evening' | 'night' {
    const hour = new Date().getHours();
    
    if (hour >= 5 && hour < 12) {
      return 'morning';
    } else if (hour >= 12 && hour < 18) {
      return 'afternoon';
    } else if (hour >= 18 && hour < 22) {
      return 'evening';
    } else {
      return 'night';
    }
  }
} 